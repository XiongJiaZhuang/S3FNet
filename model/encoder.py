## Overlapped image patch embedding with 3x3 Conv
class OverlapPatchEmbed(nn.Module):
    def __init__(self, in_c=3, embed_dim=48, bias=False):
        super(OverlapPatchEmbed, self).__init__()

        self.proj = nn.Conv2d(in_c, embed_dim, kernel_size=3,
                              stride=1, padding=1, bias=bias)

    def forward(self, x):
        x = self.proj(x)
        return x


class Encoder(nn.Module):
    def __init__(self,
                 inp_channels=8,
                 dim=32,
                 # 浅层特征融合中transformer模块的个数
                 num_blocks=4,
                 heads=2,
                 ffn_expansion_factor=2,
                 bias=False,
                 LayerNorm_type='WithBias',
                 resnet_block_nums=(1,1,1),
                 restormer_block_nums=(1,1,1),
                 ms_use_ca=False, ms_use_sa=False,
                 pan_use_ca=False, pan_use_sa=False):
        super(Encoder, self).__init__()

        self.patch_embed = OverlapPatchEmbed(inp_channels, dim)

        self.encoder_level1 = nn.Sequential(
            *[TransformerBlock(dim=dim, num_heads=heads, ffn_expansion_factor=ffn_expansion_factor,
                               bias=bias, LayerNorm_type=LayerNorm_type) for i in range(num_blocks)])
        # 在此处修改头数
        # self.fuse_branch = Encoder_RestormerBranch(dim=dim, num_blocks=restormer_block_nums, heads=(16,16,16))
        self.fuse_branch = Encoder_RestormerBranch(dim=dim, num_blocks=restormer_block_nums, heads=(2, 4, 8))
        self.spectral_branch = Encoder_ResBranch(in_planes=dim, use_ca=ms_use_ca, use_sa=ms_use_sa,
                                                 resnet_block_nums=resnet_block_nums)
        self.spatial_branch = Encoder_ResBranch(in_planes=dim, use_ca=pan_use_ca, use_sa=pan_use_sa,
                                                resnet_block_nums=resnet_block_nums)
        self.conv_frist = nn.Conv2d(in_channels=dim + dim,
                                    out_channels=dim, kernel_size=3, padding=1)

    def forward(self, ms, pan):
        inp_ms_level1 = self.patch_embed(ms)
        inp_pan_level1 = self.patch_embed(pan)

        out_ms_level1 = self.encoder_level1(inp_ms_level1)
        out_pan_level1 = self.encoder_level1(inp_pan_level1)

        fuse_feature = self.fuse_branch(self.conv_frist(torch.cat((out_ms_level1, out_pan_level1), dim=1)))

        spe_feature = self.spectral_branch(out_ms_level1)
        spa_feature = self.spatial_branch(out_pan_level1)

        return spa_feature, spe_feature, fuse_feature


